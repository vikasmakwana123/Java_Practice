package Java_basics.Java4Video;

public class RelationalOperator {
    public static void main(String[] args) {
        int a= 3;
        int b =4;
        boolean result = a>b;

        boolean areEqual= a==b;
        boolean notEqual = a!=b;

        System.out.println("Is "+a+" greater than "+b+" :- "+result);
        System.out.println("Is "+a+" equal to "+b+" :- "+areEqual);
        System.out.println("Is "+a+" not equal to "+b+" :- "+notEqual);
    }
}
